// Embeds the inkjs browser runtime (the UMD `dist/ink.js` global build) into a
// TypeScript module so the standalone HTML export can inline it — making the
// export fully offline, with no CDN dependency.
//
// Run after changing the inkjs dependency version:  npm run embed:runtime
import { readFileSync, writeFileSync } from 'node:fs'
import { createRequire } from 'node:module'
import { dirname, join } from 'node:path'

const require = createRequire(import.meta.url)

// inkjs `.` default export resolves to dist/ink.js — the UMD build that defines
// the global `inkjs` (what the exported player calls `new inkjs.Story(...)` on).
const runtimePath = require.resolve('inkjs')
const runtime = readFileSync(runtimePath, 'utf8')

// Version comes from inkjs's package.json (its exports map blocks the subpath,
// so read it from disk relative to the resolved runtime file).
const pkgPath = join(dirname(runtimePath), '..', 'package.json')
const version = JSON.parse(readFileSync(pkgPath, 'utf8')).version

const out = `// AUTO-GENERATED by scripts/embed-ink-runtime.mjs — do not edit by hand.
// Source: inkjs ${version} (dist/ink.js, the UMD global build).
// Regenerate after bumping inkjs:  npm run embed:runtime
/* eslint-disable */
export const INKJS_VERSION = ${JSON.stringify(version)}
export const INKJS_RUNTIME = ${JSON.stringify(runtime)}
`

const target = join(dirname(new URL(import.meta.url).pathname), '..', 'src', 'shared', 'inkRuntime.generated.ts')
writeFileSync(target, out, 'utf8')
console.log(`Embedded inkjs ${version} runtime (${runtime.length} bytes) -> ${target}`)
